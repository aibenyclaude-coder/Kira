/**
 * Reciprocity-gated corpus distribution (F5) — see RECIPROCITY.md.
 *
 * GET /v1/corpus              → info (tier, delay, counts)
 * GET /v1/corpus/skills.json  → Skill[]  (drop-in for the client's fetchRemote)
 * GET /v1/corpus/scars.json   → Scar[]
 *
 * Tiers:
 *   contributor / pro (valid ES256 key in Authorization: Bearer) → fresh feed
 *   keyless / invalid / expired                                  → the commons,
 *     delayed FREE_DELAY_DAYS (entries older than the window only)
 *
 * The corpus itself is MIT and ships in the public repo — this endpoint gates
 * the sanctioned *convenience channel*, not the knowledge. Freshness is the
 * premium because scar value decays as models retrain past the failure.
 *
 * Source of truth: docs/corpus.json on the default branch (generated by
 * scripts/gen-corpus.mjs, freshness-checked in CI), fetched with edge caching.
 */

const DEFAULT_SOURCE =
  "https://raw.githubusercontent.com/aibenyclaude-coder/Kira/main/docs/corpus.json";
export const FREE_DELAY_DAYS = 90;

/** Same key that ships with the npm package (src/keys/kira-pro-v1.pub.pem). */
const EMBEDDED_PUBKEY = `-----BEGIN PUBLIC KEY-----
MFkwEwYHKoZIzj0CAQYIKoZIzj0DAQcDQgAEOZCI6q9vOa6slvG3EmhIkY0rVBju
dulSwS8EWjJDDjyj7KMdPJXOLO9oi+PBfkD3KtjPlLPNgcPX4ThLAk8rxQ==
-----END PUBLIC KEY-----`;

export interface CorpusEnv {
  CORPUS_SOURCE_URL?: string;
  CORPUS_PUBKEY_PEM?: string;
}

type FeedTier = "free" | "contributor" | "pro";

interface CorpusEntry {
  id?: string;
  updated_at?: string;
  [k: string]: unknown;
}

interface CorpusBundle {
  skills?: CorpusEntry[];
  scars?: CorpusEntry[];
}

function b64uToBytes(s: string): Uint8Array {
  const padded = s.replace(/-/g, "+").replace(/_/g, "/");
  const bin = atob(padded);
  const out = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}

function pemToDer(pem: string): ArrayBuffer {
  const body = pem.replace(/-----(BEGIN|END) PUBLIC KEY-----/g, "").replace(/\s+/g, "");
  return b64uToBytes(body).buffer as ArrayBuffer;
}

/** Verify the Bearer JWT (ES256, raw ieee-p1363 sig) → effective feed tier. */
export async function verifyTierFromAuth(
  authHeader: string | null,
  pem: string
): Promise<FeedTier> {
  if (!authHeader || !authHeader.startsWith("Bearer ")) return "free";
  const token = authHeader.slice(7).trim();
  try {
    const parts = token.split(".");
    if (parts.length !== 3) return "free";
    const [h, p, s] = parts as [string, string, string];

    const header = JSON.parse(new TextDecoder().decode(b64uToBytes(h)));
    if (header.alg !== "ES256") return "free";

    const key = await crypto.subtle.importKey(
      "spki",
      pemToDer(pem),
      { name: "ECDSA", namedCurve: "P-256" },
      false,
      ["verify"]
    );
    const ok = await crypto.subtle.verify(
      { name: "ECDSA", hash: "SHA-256" },
      key,
      b64uToBytes(s),
      new TextEncoder().encode(`${h}.${p}`)
    );
    if (!ok) return "free";

    const claims = JSON.parse(new TextDecoder().decode(b64uToBytes(p)));
    if (claims.iss !== "kira.sh") return "free";
    if (typeof claims.exp !== "number" || claims.exp < Date.now() / 1000) return "free";
    if (claims.tier === "pro") return "pro";
    if (claims.tier === "contributor") return "contributor";
    return "free";
  } catch {
    return "free";
  }
}

function gateForTier(entries: CorpusEntry[] | undefined, tier: FeedTier): CorpusEntry[] {
  const list = entries ?? [];
  if (tier !== "free") return list;
  const cutoff = Date.now() - FREE_DELAY_DAYS * 24 * 3600 * 1000;
  return list.filter((e) => {
    const t = Date.parse(e.updated_at ?? "");
    return Number.isFinite(t) && t <= cutoff;
  });
}

export async function corpus(
  req: Request,
  env: CorpusEnv,
  sub: "" | "skills.json" | "scars.json"
): Promise<Response> {
  const pem = env.CORPUS_PUBKEY_PEM ?? EMBEDDED_PUBKEY;
  const tier = await verifyTierFromAuth(req.headers.get("Authorization"), pem);

  const source = env.CORPUS_SOURCE_URL ?? DEFAULT_SOURCE;
  let bundle: CorpusBundle;
  try {
    const res = await fetch(source, {
      cf: { cacheTtl: 600, cacheEverything: true },
    } as RequestInit);
    if (!res.ok) {
      return new Response(
        JSON.stringify({ error: { code: "upstream", message: `corpus source ${res.status}` } }),
        { status: 502, headers: { "Content-Type": "application/json" } }
      );
    }
    bundle = (await res.json()) as CorpusBundle;
  } catch {
    return new Response(
      JSON.stringify({ error: { code: "upstream", message: "corpus source unreachable" } }),
      { status: 502, headers: { "Content-Type": "application/json" } }
    );
  }

  const skills = gateForTier(bundle.skills, tier);
  const scars = gateForTier(bundle.scars, tier);

  const headers: HeadersInit = {
    "Content-Type": "application/json",
    "X-Kira-Tier": tier,
    "X-Kira-Delay-Days": tier === "free" ? String(FREE_DELAY_DAYS) : "0",
    // Tier-dependent bodies must never mix in shared caches.
    "Cache-Control": tier === "free" ? "public, max-age=600" : "no-store",
    Vary: "Authorization",
  };

  if (sub === "skills.json") return new Response(JSON.stringify(skills), { status: 200, headers });
  if (sub === "scars.json") return new Response(JSON.stringify(scars), { status: 200, headers });

  return new Response(
    JSON.stringify({
      tier,
      delay_days: tier === "free" ? FREE_DELAY_DAYS : 0,
      skills: skills.length,
      scars: scars.length,
      reciprocity:
        "Share one accepted scar → contributor key → fresh feed free. " +
        "Or support the project for a supporter key. Everything joins the delayed commons anyway — see RECIPROCITY.md.",
    }),
    { status: 200, headers }
  );
}
